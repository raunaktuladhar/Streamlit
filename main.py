import streamlit as st

# Title
st.title("Hi, I am Raunak, Who the hell are you?")

# subheader
st.subheader("Yo!")

# header
st.header("Hehe")

# text
# text is just like paragraph tag in HTML
st.text("Raunak Bir Singh Tuladhar")

# markdown
# this markdown is a bit similar like in jupyter notebook
st.markdown("**Hello** World")  # '**' makes text bold
st.markdown("Hello *World*")    # '*' makes italic

# caption
st.caption("Caption")